using Microsoft.AspNetCore.Mvc;
using Microsoft.EntityFrameworkCore;
using Backend.Data;
using Backend.Models;

namespace Backend.Controllers
{
    [Route("api/[controller]")]
    [ApiController]
    public class CompaniesController(DatabaseContext context) : ControllerBase
    {
        private readonly DatabaseContext _context = context;

        // GET: api/companies/5/5
        [HttpGet("{limit:max(10):min(1)}/{offset}")]
        public async Task<ActionResult<PagedResponse<Company>>> GetCompanies(int limit, int offset)
        {
            var pagedResponse = new PagedResponse<Company>()
            {
                Data = await context.Companies.Where(company => company.DeletedAt == null).Skip(offset).Take(limit).OrderBy(company => company.CreatedAt)
                    .ToListAsync(),
                Meta = new Meta()
                {
                    TotalCount = await context.Companies.CountAsync(company => company.DeletedAt == null)
                }
            };

            return pagedResponse;
        }

        // GET: api/Companies/5
        [HttpGet("{id}")]
        public async Task<ActionResult<Company>> GetCompany(int id)
        {
            var company = await _context.Companies.FindAsync(id);

            if (company == null)
            {
                return NotFound();
            }

            return company;
        }

        // PUT: api/Companies/5
        // To protect from overposting attacks, see https://go.microsoft.com/fwlink/?linkid=2123754
        [HttpPut("{id}")]
        public async Task<IActionResult> PutCompany(int id, Company company)
        {
            if (id != company.Id)
            {
                return BadRequest();
            }

            company.UpdatedAt = DateTime.Now;
            _context.Entry(company).State = EntityState.Modified;

            try
            {
                await _context.SaveChangesAsync();
            }
            catch (DbUpdateConcurrencyException)
            {
                if (!CompanyExists(id))
                {
                    return NotFound();
                }
                else
                {
                    throw;
                }
            }

            return NoContent();
        }

        // POST: api/Companies
        // To protect from overposting attacks, see https://go.microsoft.com/fwlink/?linkid=2123754
        [HttpPost]
        public async Task<ActionResult<Company>> PostCompany(Company company)
        {
            _context.Companies.Add(company);
            await _context.SaveChangesAsync();

            return CreatedAtAction("GetCompany", new { id = company.Id }, company);
        }

        // DELETE: api/Companies/5
        [HttpDelete("{id}")]
        public async Task<IActionResult> DeleteCompany(int id)
        {
            var company = await _context.Companies.FindAsync(id);
            if (company == null)
            {
                return NotFound();
            }

            company.DeletedAt = DateTime.Now;

            context.Entry(company).State = EntityState.Modified;

            await context.SaveChangesAsync();

            return NoContent();
        }

        private bool CompanyExists(int id)
        {
            return _context.Companies.Any(e => e.Id == id);
        }
    }
}
